    private void jButton1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton1MouseClicked
        // TODO add your handling code here:
        log = false;
        for (int i = 0; i < users.size(); i++) {
            if (users.get(i).getContrase単a().equals(logpass.getText()) && users.get(i).getUser().equals(loguser.getText())) {
                
                log = true;
                s = users.get(i);
                pcorreo.setText(s.getEmail());
                pnombre.setText(s.getNombre());
                pedad.setText(s.getEdad());
            }
            
        }
        if (log == true) {
            jd_logeado.setModal(true);
            jd_logeado.pack();
            jd_logeado.setLocationRelativeTo(this);
            jd_logeado.setVisible(true);
        } else {
            JOptionPane.showMessageDialog(this, "Contrase単a o Usuario incorrecto");
        }
    }//GEN-LAST:event_jButton1MouseClicked

